package fifoanimalshelter;

/**
 * A special type of Animal - cat!
 */
public class Cat extends Animal {

    /**
     * Default constructor.
     */
    public Cat() {
        super();
    }

    /**
     * Constructor.
     * @param name The cat's name.
     */
    public Cat(String name) {
        super(name);
    }
}
